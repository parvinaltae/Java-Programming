package day03_escape_sequence;


    public class EscapeSequence2 {
        public static void main(String[] args){
            System.out.println("\"iphone case\", \"xbox\",\"monitor\"");
            System.out.println("\"iphone case\", \"iphone\",\n \"xbox\", \"monitor\"");
            System.out.println("\"monitor\"");
            System.out.println("  ^  ");
            System.out.println("/   \\");
            System.out.println("|   |");
            System.out.println("_____");
        }
    }